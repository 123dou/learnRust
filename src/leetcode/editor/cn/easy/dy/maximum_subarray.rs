impl Solution {
    pub fn max_sub_array(nums: Vec<i32>) -> i32 {
        match nums.len() {
            0 => 0,
            _ => {
                let mut res = i32::MIN;
                let mut sum = 0;
                for val in nums.iter() {
                    sum += *val;
                    res = res.max(sum.max(*val));
                    if sum < *val {
                        sum = *val;
                    }
                }
                res
            }
        }
    }
}
