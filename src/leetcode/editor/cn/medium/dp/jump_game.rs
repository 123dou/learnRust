impl Solution {
    pub fn can_jump(nums: Vec<i32>) -> bool {
        let mut index = 0;
        let mut max_reach_pos = 0;
        while index < nums.len() {
            max_reach_pos = max_reach_pos.max(index + nums[index] as usize);
            if max_reach_pos >= nums.len() - 1 {
                return true;
            }
            if max_reach_pos <= index && nums[index] == 0 {
                break;
            }
            index += 1;
        }
        false
    }
}
